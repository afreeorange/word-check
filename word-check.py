for word in text.split(" "):
    cached_segment = word

    word = word.replace(".", "")
    word = word.replace(",", "")
    word = word.replace(":", "")
    word = word.replace('"', "")
    word = word.replace("!", "")
    word = word.replace(")", "")
    word = word.replace("(", "")
    word = word.replace("'", "")
    word = word.lower()

    # Yeah... I'm lazy. "It works".
    if word not in COMMON_WORDS:
        sys.stdout.write(COLOR_RED + cached_segment + COLOR_END)
    else:
        sys.stdout.write(cached_segment)

